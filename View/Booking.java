    private void bookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookingActionPerformed
        // TODO add your handling code here:
        int selectedRow = TicketTable.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a ticket to book.");
        return;
    }

    // Retrieve the ticketId from the selected row
    String ticketId = TicketTable.getValueAt(selectedRow, 0).toString();

    // Assuming username is from a label or logged-in user session
    String username = loggedUserLabel.getText(); // or replace with sessionUsername if applicable

    if (username.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Oops, no user found!");
        return;
    }

    // Call DAO to book the ticket
    BookedTicketDao dao = new BookedTicketDao();
    boolean success = dao.bookTicket(username, ticketId);

    if (success) {
        JOptionPane.showMessageDialog(this, "Ticket booked successfully!");
        
        // Show confirmation dialog with options
        int confirm = JOptionPane.showConfirmDialog(this,
            "Ticket booked successfully! Do you want to download your ticket?",
            "Ticket Booking", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        
        // If user clicks 'Yes', call print function
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                printSelectedTicket(username, ticketId);
                
            } catch (PrinterException ex) {
                Logger.getLogger(Booking.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    } else {
        JOptionPane.showMessageDialog(this, "Failed to book ticket. Please try again.");
    }
    }//GEN-LAST:event_bookingActionPerformed

