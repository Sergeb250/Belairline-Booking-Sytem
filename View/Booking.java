    private void BsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BsearchActionPerformed
        // TODO add your handling code here:
        
        String origin = orginTxt.getText().trim();
    String destination = destinationTxt.getText().trim();
    String category = null;

    // Determine selected category
    if (fistR.isSelected()) {
        category = "First Class";
    } else if (businessR.isSelected()) {
        category = "Business";
    } else if (economicR.isSelected()) {
        category = "Economic";
    }

    // Check if all fields are filled
    if (origin.isEmpty() || destination.isEmpty() || category == null) {
        JOptionPane.showMessageDialog(this, "Please fill all fields and select a class.");
        return;
    }

    // Call DAO
    TicketDao dao = new TicketDao();
    List<Ticket> result = dao.searchTickets(origin, destination, category);

    if (result.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No matching tickets found.");
        loadTicketsToTable();  // Load all again after OK
    } else {
        // Update table
        DefaultTableModel model = (DefaultTableModel) TicketTable.getModel();
        model.setRowCount(0); // Clear table first

        for (Ticket ticket : result) {
            model.addRow(new Object[]{
                ticket.getTicketId(),
                ticket.getOrigin(),
                ticket.getDestination(),
                ticket.getDate(),
                ticket.getPrice(),
                ticket.getCategory(),
                ticket.getSeat()
            });
        }
    }

    this.toFront(); // Ensure the frame is in front
    }//GEN-LAST:event_BsearchActionPerformed

