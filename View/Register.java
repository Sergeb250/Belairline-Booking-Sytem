    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        // TODO add your handling code here:
        
 // Check if any field is empty
    if (firstTxt.getText().isEmpty() || lastTxt.getText().isEmpty() || phoneTxt.getText().isEmpty() ||
        addressTxt.getText().isEmpty() || usernameTxt.getText().isEmpty() || passTxt.getText().isEmpty()) {

        setCustomDialogStyle();
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        return;
    }

    // Setting model object
    User usr = new User();
    usr.setFirstName(firstTxt.getText());
    usr.setLastName(lastTxt.getText());
    usr.setPhoneNumber(phoneTxt.getText());
    usr.setAddress(addressTxt.getText());
    usr.setUserName(usernameTxt.getText());
    usr.setPassword(passTxt.getText());

    UserDao dao = new UserDao();

    // Check if username already exists
    if (dao.usernameExists(usr.getUserName())) {
        setCustomDialogStyle();
        JOptionPane.showMessageDialog(this, "Username already exists! Please choose another one.");
        clearusr(); // Clear only user info fields
        return;
    }

    // Try registering
    boolean rowAffected = dao.registerUser(usr);

    setCustomDialogStyle();

    if (rowAffected) {
        JOptionPane.showMessageDialog(this, "Your Registration on BelairLines succeeded");

        JFrame currentFrame = (JFrame) SwingUtilities.getWindowAncestor((Component) evt.getSource());
        LOGIN loginFrame = new LOGIN();
        loginFrame.setVisible(true);
        currentFrame.dispose();

    } else {
        JOptionPane.showMessageDialog(this, "Registration failed. Please try again.");
    }
   
   
        
    }//GEN-LAST:event_registerActionPerformed

